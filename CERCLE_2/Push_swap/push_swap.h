//Node struct double linked list
typedef struct s_node
{
	int	value;
	struct s_node	*prev;
	struct s_node	*next;
}	t_node;

//Stack struct
typedef struct s_stack
{
	t_node	*top;
	t_node	*bottom;
	int	size;
}	t_stack;


